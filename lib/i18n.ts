export type UIStringKey = keyof typeof UI_STRINGS.en;

export function t(key: UIStringKey, lang: Lang): string {
  const row = UI_STRINGS[lang];
  return (row[key] as string) ?? UI_STRINGS.en[key];
}

const CATEGORY_TO_KEY: Record<string, UIStringKey | undefined> = {
  ALIGNED: 'aligned',
  STABLE: 'stable',
  TENSION: 'tension',
  FRACTURE: 'fracture',
  INVERSION: 'inversion',
};

/** Display label for NAI category codes; keeps raw value if unknown. */
export function categoryLabel(raw: string, lang: Lang): string {
  const u = (raw || '').toUpperCase().replace(/\s+/g, '_');
  const key = CATEGORY_TO_KEY[u];
  if (key) return t(key, lang);
  return raw || '—';
}
