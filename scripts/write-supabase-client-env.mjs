#!/usr/bin/env node
/**
 * Writes lib/supabase/env.client.generated.ts from process env so OpenNext/CF
 * client bundles embed Supabase URL + anon/publishable key.
 *
 * - Loads .env.local / .env.production / .env when present (local dev).
 * - GitHub Actions: secrets are already in env before `npm run build:cf`.
 * - If no URL+key in env, exits 0 and leaves the repo file unchanged (empty placeholders).
 */

import { config } from 'dotenv';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');

for (const name of ['.env.local', '.env.production', '.env']) {
  config({ path: path.join(root, name) });
}

const url = (process.env.NEXT_PUBLIC_SUPABASE_URL || '').trim();
const key = (
  process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY ||
  process.env.NEXT_PUBLIC_SUPABASE_PUBLISHABLE_KEY ||
  ''
).trim();

const out = path.join(root, 'lib/supabase/env.client.generated.ts');

if (!url || !key) {
  console.log(
    '[write-supabase-client-env] Skipping: no NEXT_PUBLIC_SUPABASE_URL + key in env (CI may generate this file in an earlier step).'
  );
  process.exit(0);
}

const allowWrite =
  process.env.CI === 'true' ||
  process.env.GITHUB_ACTIONS === 'true' ||
  process.env.FORCE_WRITE_SUPABASE_CLIENT_ENV === '1';

if (!allowWrite) {
  console.log(
    '[write-supabase-client-env] Skipping file write locally (avoids committing secrets). Next.js still inlines NEXT_PUBLIC_* from .env.local. In CI, GITHUB_ACTIONS is set and this file is written.'
  );
  process.exit(0);
}

const body =
  '/**\n' +
  ' * Build-time Supabase env for the client bundle.\n' +
  ' * Generated by scripts/write-supabase-client-env.mjs — do not commit real keys.\n' +
  ' */\n' +
  `export const NEXT_PUBLIC_SUPABASE_URL = ${JSON.stringify(url)};\n` +
  `export const NEXT_PUBLIC_SUPABASE_ANON_KEY = ${JSON.stringify(key)};\n`;

fs.writeFileSync(out, body, 'utf8');
console.log('[write-supabase-client-env] Wrote', path.relative(root, out));
